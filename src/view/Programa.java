    private void jbGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGerarActionPerformed
        // instancia a classe GeraVetor
        GeraVetor gv = new GeraVetor();
        
        if ( jtfTamanho.getText().isEmpty() ){
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo tamanho!");
        }
        else {
            
            int tamanho = Integer.parseInt(jtfTamanho.getText());
            
            if ( jrbCrescente.isSelected() ){
                int[] vetor = new int[tamanho];
                
                vetor = gv.gerarVetorCrescente(tamanho); 
                try {
                    gv.geraVetorTxt(vetor);
                    JOptionPane.showMessageDialog(rootPane, "Vetor crescente de "+tamanho+" posições gerado!");
                    jtaVetorGerado.setText(cv.vetorGerado("vetor.txt"));
                } catch (IOException ex) {
                    Logger.getLogger(Programa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            else if ( jrbDecrescente.isSelected() ){
                    int[] vetor = new int[tamanho];
                    
                    vetor = gv.gerarVetorDecrescente(tamanho);
                try {
                    gv.geraVetorTxt(vetor);
                    JOptionPane.showMessageDialog(rootPane, "Vetor decrescente de "+tamanho+" posições gerado!");
                    jtaVetorGerado.setText(cv.vetorGerado("vetor.txt"));
                } catch (IOException ex) {
                    Logger.getLogger(Programa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            else if ( jrbAleatorio.isSelected() ){
                int[] vetor = new int[tamanho];
                
                if ( jtfVariedade.getText().isEmpty() ){
                    JOptionPane.showMessageDialog(rootPane, "Preencha o campo variedade!");
                }
                else {
                    int variedade = Integer.parseInt(jtfVariedade.getText());
                
                    vetor = gv.gerarVetorAleatorio(tamanho, variedade); 
                    try {
                        gv.geraVetorTxt(vetor);
                        JOptionPane.showMessageDialog(rootPane, "Vetor aleatório de "+tamanho+" posições gerado!");
                        jtaVetorGerado.setText(cv.vetorGerado("vetor.txt"));
                    } catch (IOException ex) {
                        Logger.getLogger(Programa.class.getName()).log(Level.SEVERE, null, ex);
                    }  
                }                
            }
            
        } 
        
    }//GEN-LAST:event_jbGerarActionPerformed

