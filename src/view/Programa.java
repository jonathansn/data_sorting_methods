    private void jbBubbleSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBubbleSortActionPerformed
        int tamanho = Integer.parseInt(jtfTamanho.getText());
        
        BubbleSort bs = new BubbleSort();        
        
        try {
            int vetorD[] = gv.lerTxtGerado(tamanho,"vetor.txt");
            
            long tempoInicial = System.currentTimeMillis();
            bs.ordenar(vetorD);
            long tempoFinal = System.currentTimeMillis();
            System.out.printf("%.3f ms%n", (tempoFinal - tempoInicial) / 1000d);
            String tempo = String.valueOf((tempoFinal - tempoInicial) / 1000d);
            
            jlComp.setText(String.valueOf(bs.getComparacoes()));
            jlTroca.setText(String.valueOf(bs.getTrocas()));
            jlTempExec.setText(tempo);
            
            gv.geraTxtOrdenado(vetorD);
            
            jtaVetorOrdenado.setText(cv.vetorOrdenado("vetorOrdenado.txt"));
            
            JOptionPane.showMessageDialog(rootPane, "Vetor Ordenado por Bubble Sort!");
            
        } catch (IOException ex) {
            Logger.getLogger(Programa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbBubbleSortActionPerformed

